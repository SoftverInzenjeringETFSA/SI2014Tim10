    private void btnOdaberiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOdaberiActionPerformed
        clearError();
        
        DefaultTableModel model = ((DefaultTableModel) this.jTable1.getModel());
        
        int selectedRow = this.jTable1.getSelectedRow();
        
        if (selectedRow != -1)
        {
            Klijent klijent = _klijenti.get(selectedRow);
            
            _parent.setKlijent(klijent);
            
            this.dispose();
        }
        else
        {
            setErrorMsg("Potrebno je odabrati klijenta.");
        }
    }//GEN-LAST:event_btnOdaberiActionPerformed

    private void getKlijenti() {
        _klijenti = _utils.getKlijenti();
        
        if (_klijenti.size() > 0)
        {
            updateTable();
        }
        else
        {
            ((DefaultTableModel) this.jTable1.getModel()).setRowCount(0);
        }
    }
    
    private void updateTable() {
        
        DefaultTableModel model = ((DefaultTableModel) this.jTable1.getModel());
        
        model.setRowCount(0);
        Object[] dataRow;
        
        for(Klijent klijent: _klijenti)
        {
            dataRow = new Object[]{
                klijent.getId(),
                klijent.getNaziv(),
                klijent.getSifraKlijenta(),
                klijent.getAdresa(),
                klijent.getTip()
            };
            
            model.addRow(dataRow);
        }
    }
    
