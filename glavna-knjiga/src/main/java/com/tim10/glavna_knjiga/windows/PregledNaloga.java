    private void getNalozi(boolean isInit) {
        _nalozi = _utils.getNalozi(this.txtFilterSifra.getText(), this.txtFilterNaziv.getText(), this.cbFilterStatus.getSelectedIndex(), isInit);
        
        if (_nalozi.size() > 0)
        {
            updateTable(_nalozi);
        }
        else
        {
            ((DefaultTableModel) this.tblNalozi.getModel()).setRowCount(0);
        }
    }
    
    private void updateTable(List<Nalozi> nalozi) {
        
        DefaultTableModel model = ((DefaultTableModel) this.tblNalozi.getModel());
        
        model.setRowCount(0);
        Object[] dataRow;
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        
        for(Nalozi nalog: nalozi)
        {
            dataRow = new Object[]{
                nalog.getId(),
                nalog.getSifraNaloga(),
                nalog.getNaziv(),
                (nalog.getStatus() == 1) ? "OTVOREN" : "UPISAN",
                dateFormat.format(nalog.getDatumKreiranja()),
                (nalog.getDatumKnjizenja() != null) ? dateFormat.format(nalog.getDatumKnjizenja()) : ""
            };
            
            model.addRow(dataRow);
        }
    }
    
