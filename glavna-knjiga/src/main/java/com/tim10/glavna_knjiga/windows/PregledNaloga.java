    private void btnIzbrišiNalogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzbrišiNalogActionPerformed

        clearErrorMsg();
                
        int selectedRow = this.tblNalozi.getSelectedRow();
        DefaultTableModel model = ((DefaultTableModel) this.tblNalozi.getModel());
        
        if (selectedRow == -1)
        {
            setErrorMsg("Potrebno je selektovati nalog za brisanje.");
            
            return;
        }
        
        if (model.getValueAt(selectedRow, 3).toString().equals("UPISAN"))
        {
            setErrorMsg("Nije moguće obrisati upisan nalog.");
            
            return;
        }
        
        int idNalogaZaBrisanje = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());
        
        for (Nalozi delNalog: _nalozi)
        {
            if (delNalog.getId() == idNalogaZaBrisanje)
            {
                _utils.obrisiNalog(delNalog);
                
                model.removeRow(selectedRow);
                
                JOptionPane.showMessageDialog(this, String.format("Nalog sa šifrom: %s uspješno obrisan.", delNalog.getSifraNaloga()), null, JOptionPane.INFORMATION_MESSAGE);
                
                break;
            }
        }
    }//GEN-LAST:event_btnIzbrišiNalogActionPerformed

    private void btnOtvoriNalogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOtvoriNalogActionPerformed
        clearErrorMsg();
                
        int selectedRow = this.tblNalozi.getSelectedRow();
        DefaultTableModel model = ((DefaultTableModel) this.tblNalozi.getModel());
        
        if (selectedRow == -1)
        {
            setErrorMsg("Potrebno je selektovati nalog.");
            
            return;
        }
        
        int idNaloga = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());
        
        for (Nalozi nalog: _nalozi)
        {
            if (nalog.getId() == idNaloga)
            {
                this.setEnabled(false);
                
                Nalog nalogFrame = new Nalog(this, nalog);
                nalogFrame.setVisible(true);
                
                break;
            }
        }
    }//GEN-LAST:event_btnOtvoriNalogActionPerformed

