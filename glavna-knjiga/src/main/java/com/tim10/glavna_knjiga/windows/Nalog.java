    private void recalculateFields() {
        DefaultTableModel model = ((DefaultTableModel) this.tblStavkeNaloga.getModel());
        Double duguje = 0.00, potrazuje = 0.00;
        
        if (model.getRowCount() != 0)
        {
            for (int i = 0; i < model.getRowCount(); i++)
            {
                duguje += Double.parseDouble(model.getValueAt(i, 4).toString().replace(",", ""));
                potrazuje += Double.parseDouble(model.getValueAt(i, 5).toString().replace(",", ""));
            }
        }
        
        this.txtDuguje.setValue(duguje);
        this.txtPotrazuje.setValue(potrazuje);
        this.txtSaldo.setValue(duguje - potrazuje);
    }
    
    private void disableForm(){
        this.txtNazivNaloga.setEditable(false);
        this.txtNazivNaloga.setBackground(Color.gray);
        this.txtSifraNaloga.setEditable(false);
        this.txtSifraNaloga.setBackground(Color.gray);
        this.btnDodajStavku.setEnabled(false);
        this.btnObrisiNalog.setEnabled(false);
        this.btnObrisiStavku.setEnabled(false);
        this.btnSpasiNalog.setEnabled(false);
        this.btnUpisiNalog.setEnabled(false);
        this.btnUrediStavku.setEnabled(false);
        this.tblStavkeNaloga.setEnabled(false);
    }
    
    private boolean validateUpis() {
        if (this.txtNazivNaloga.getText().isEmpty())
        {
            setErrorMsg("Potrebno unijeti sva polja.");

            return false;
        }

        if (this.tblStavkeNaloga.getRowCount() == 0)
        {
            setErrorMsg("Nalog nema stavki za knjiženje.");

            return false;
        }

        if (Double.parseDouble(this.txtSaldo.getText().replace(",", "")) != 0)
        {
            setErrorMsg("Ukupno dugovanje mora biti jednako ukupnom potraživanju.");

            return false;
        }
        
        return true;
    }
            
