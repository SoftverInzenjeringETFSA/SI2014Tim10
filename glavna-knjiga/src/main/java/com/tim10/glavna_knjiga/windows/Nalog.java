    private void btnObrisiStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiStavkuActionPerformed
        
        clearError();
        
        DefaultTableModel model = ((DefaultTableModel) this.tblStavkeNaloga.getModel());
        
        int selectedRow = this.tblStavkeNaloga.getSelectedRow();
        
        if (selectedRow != -1)
        {
            int stavkaId = (Integer)model.getValueAt(selectedRow, 6);
            
            if (stavkaId != -1)
            {
                _obrisaneStavkeIDs.add(stavkaId);
            }
            
            model.removeRow(this.tblStavkeNaloga.getSelectedRow());
            
            recalculateFields();
        }
        else
        {
            if (this.tblStavkeNaloga.getRowCount() == 0)
                this.setErrorMsg("Nema unešenih stavki.");
            else
                this.setErrorMsg("Potrebno je selektovati stavku za brisanje.");
        }
    }//GEN-LAST:event_btnObrisiStavkuActionPerformed

    private void btnUrediStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUrediStavkuActionPerformed
        
        clearError();
        
        DefaultTableModel model = ((DefaultTableModel) this.tblStavkeNaloga.getModel());
        
        int selectedRow = this.tblStavkeNaloga.getSelectedRow();
        
        if (selectedRow != -1)
        {
            StavkeNaloga stavka = new StavkeNaloga();
            int stavkaId = (Integer)model.getValueAt(selectedRow, 6);
            
            if (stavkaId != -1)
            {
                for(StavkeNaloga st: _nalog.getStavkeNalogas())
                {
                    if(st.getId() == stavkaId)
                    {
                        stavka = st;
                        
                        break;
                    }
                }
                
                if (stavka.getId() > 0)
                {
                    Stavka editStavka = new Stavka(this, stavka, model.getValueAt(selectedRow, 2).toString(), model.getValueAt(selectedRow, 0).toString());

                    this.setEnabled(false);
                    editStavka.setVisible(true);
                }
            }
            else 
            {
                Stavka editStavka = new Stavka(this, model.getValueAt(selectedRow, 1).toString(), model.getValueAt(selectedRow, 2).toString(), 
                        model.getValueAt(selectedRow, 0).toString(), model.getValueAt(selectedRow, 4).toString(), model.getValueAt(selectedRow, 5).toString());
      
                this.setEnabled(false);
                editStavka.setVisible(true);
            }
        }
        else
        {
            if (this.tblStavkeNaloga.getRowCount() == 0)
                this.setErrorMsg("Nema unešenih stavki.");
            else
                this.setErrorMsg("Potrebno je selektovati stavku.");
        }
    }//GEN-LAST:event_btnUrediStavkuActionPerformed

    private void btnSpasiNalogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSpasiNalogActionPerformed
        
        clearError();
        
        try
        {
            if (_nalog.getStatus() == 0)
            {
                if (this.txtNazivNaloga.getText().isEmpty() || this.txtSifraNaloga.getText().isEmpty())
                {
                    setErrorMsg("Potrebno unijeti sva polja.");

                    return;
                }

                _nalog.setNaziv(this.txtNazivNaloga.getText());
                _nalog.setSifraNaloga(this.txtSifraNaloga.getText());

                _utils.spasiNalog(_nalog, false);

                JOptionPane.showMessageDialog(this, String.format("Nalog sa šifrom: %s uspješno spašen.", _nalog.getSifraNaloga()), null, JOptionPane.INFORMATION_MESSAGE);
                
                this.dispose();
            }
            else
            {
                if (this.txtNazivNaloga.getText().isEmpty())
                {
                    setErrorMsg("Potrebno unijeti sva polja.");

                    return;
                }
                
                _nalog.setNaziv(this.txtNazivNaloga.getText());
                _utils.izmijeniNalog(_nalog, false);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Greška prilikom spašavanja naloga.", "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSpasiNalogActionPerformed

