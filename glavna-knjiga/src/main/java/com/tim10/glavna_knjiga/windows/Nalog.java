    private void btnUpisiNalogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpisiNalogActionPerformed
        clearError();
        
        try
        {
            if (_nalog.getStatus() == 0)
            {
                if (!validateUpis())
                    return;

                _nalog.setNaziv(this.txtNazivNaloga.getText());
                _nalog.setSifraNaloga(this.txtSifraNaloga.getText());

                _utils.spasiNalog(_nalog, true);

                JOptionPane.showMessageDialog(this, String.format("Nalog sa šifrom: %s uspješno zaključen.", _nalog.getSifraNaloga()), null, JOptionPane.INFORMATION_MESSAGE);
                
                this.dispose();
            }
            else
            {
                if (!validateUpis())
                    return;
                
                _nalog.setNaziv(this.txtNazivNaloga.getText());
                _utils.izmijeniNalog(_nalog, true);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Greška prilikom zaključivanja naloga.", "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpisiNalogActionPerformed
